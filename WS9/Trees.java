
public class Trees {
	private double[] xTrees;
	private double[] yTrees;
	private double[] scaleTrees;
	public Trees(double[] xTrees, double[] yTrees, double[] scaleTrees) {
		this.xTrees = xTrees;
		this.yTrees = yTrees;
		this.scaleTrees = scaleTrees;
	}
	public double[] getxTrees() {
		return xTrees;
	}
	public void setxTrees(double[] xTrees) {
		this.xTrees = xTrees;
	}
	public double[] getyTrees() {
		return yTrees;
	}
	public void setyTrees(double[] yTrees) {
		this.yTrees = yTrees;
	}
	public double[] getScaleTrees() {
		return scaleTrees;
	}
	public void setScaleTrees(double[] scaleTrees) {
		this.scaleTrees = scaleTrees;
	}
	
	
}
